package mycucu;

public class Account {

	private int accountNbr;
	private int accountBal;
	private int cardNbr;
	
	public int getCardNbr() {
		return cardNbr;
	}
	public void setCardNbr(int cardNbr) {
		this.cardNbr = cardNbr;
	}
	public int getAccountNbr() {
		return accountNbr;
	}
	public void setAccountNbr(int accountNbr) {
		this.accountNbr = accountNbr;
	}
	public int getAccountBal() {
		return accountBal;
	}
	public void setAccountBal(int accountBal) {
		this.accountBal = accountBal;
	}
	
}
